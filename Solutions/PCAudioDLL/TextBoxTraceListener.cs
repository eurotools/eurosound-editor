using System.Diagnostics;
using System.Windows.Forms;

namespace PCAudioDLL
{
    //-------------------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------------------
    public class TextBoxTraceListener : TraceListener
    {
        private readonly TextBox tBox;
        internal bool PauseOutput { get; set; }

        //-------------------------------------------------------------------------------------------------------------------------------
        public TextBoxTraceListener(TextBox box)
        {
            this.tBox = box;
        }

        //-------------------------------------------------------------------------------------------------------------------------------
        public override void Write(string msg)
        {
            //allows tBox to be updated from different thread
            try
            {
                if (!PauseOutput)
                {
                    tBox.Parent.Invoke(new MethodInvoker(delegate ()
                    {
                        tBox.AppendText(msg);
                    }));
                }
            }
            catch
            {

            }
        }

        //-------------------------------------------------------------------------------------------------------------------------------
        public override void WriteLine(string msg)
        {
            Write(msg + "\r\n");
        }
    }

    //-------------------------------------------------------------------------------------------------------------------------------
}
