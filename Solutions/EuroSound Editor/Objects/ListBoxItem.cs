namespace sb_editor
{
    //-------------------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------------------
    public class ListBoxItem
    {
        private string _ItemText;
        private string _ItemID;

        //-------------------------------------------------------------------------------------------------------------------------------
        public ListBoxItem(string ItemText, string ItemID)
        {
            _ItemText = ItemText;
            _ItemID = ItemID;
        }

        //-------------------------------------------------------------------------------------------------------------------------------
        public string Text
        {
            get { return _ItemText; }
            set { _ItemText = value; }
        }

        //-------------------------------------------------------------------------------------------------------------------------------
        public string ItemData
        {
            get { return _ItemID; }
            set { _ItemID = value; }
        }
    }

    //-------------------------------------------------------------------------------------------------------------------------------
}
